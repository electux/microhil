#pragma once

#include <com/ilib.h>
#include <memory>
#include <string>
#include <vector>

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @namespace Electux::App::Com
/// @brief Namespace for application communication components
namespace Electux::App::Com
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// @class LibSerialPortWrapper
	/// @brief Wrapper class for LibSerial::SerialPort implementing ILibSerialPort.
	///
	/// This class provides an abstraction layer over the LibSerial::SerialPort
	/// library, enabling easier integration and testing.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	class LibSerialPortWrapper : public ILibSerialPort
	{
	public:
		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Constructs a new LibSerialPortWrapper object.
		////////////////////////////////////////////////////////////////////////////////////////////////
		LibSerialPortWrapper();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Destroys the LibSerialPortWrapper object.
		////////////////////////////////////////////////////////////////////////////////////////////////
		~LibSerialPortWrapper() override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Opens the serial port with the specified device.
		/// @param device The name of the device to open.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void Open(const std::string &device) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Closes the serial port.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void Close() override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Checks if the serial port is open.
		/// @return True if the serial port is open, false otherwise.
		////////////////////////////////////////////////////////////////////////////////////////////////
		bool IsOpen() const override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Reads data from the serial port.
		/// @param data A vector to store the read data.
		/// @param len The number of bytes to read.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void Read(std::vector<uint8_t> &data, size_t len) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Writes data to the serial port.
		/// @param data The data to write.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void Write(const std::vector<uint8_t> &data) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Sets the baud rate of the serial port.
		/// @param baudRate The baud rate to set.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void SetBaudRate(BaudRate baudRate) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Sets the character size of the serial port.
		/// @param characterSize The character size to set.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void SetCharacterSize(CharacterSize characterSize) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Sets the parity of the serial port.
		/// @param parity The parity to set.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void SetParity(Parity parity) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Sets the stop bits of the serial port.
		/// @param stopBits The stop bits to set.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void SetStopBits(StopBits stopBits) override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Sets the flow control of the serial port.
		/// @param flowControl The flow control to set.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void SetFlowControl(FlowControl flowControl) override;

	private:
		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Unique pointer to the underlying LibSerial::SerialPort instance.
		////////////////////////////////////////////////////////////////////////////////////////////////
		std::unique_ptr<SerialPort> m_serialPort;
	};
} // namespace Electux::App::Com
