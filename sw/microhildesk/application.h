#pragma once

#include <memory>
#include <gtkmm/application.h>
#include <view/home.h>
#include <view/settings/settings.h>
#include <view/help/help.h>
#include <view/about/about.h>
#include <view/settings_setup.h>
#include <config/iconfig.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @namespace Electux::App
/// @brief Main namespace for the microhildesk application
namespace Electux::App
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// @name Local Type Aliases
	/// @{
	using IConfig = Electux::App::Config::IConfig;
	using AppHome = Electux::App::View::AppHome;
	using AppSettings = Electux::App::View::Settings::AppSettings;
	using AppHelp = Electux::App::View::Help::AppHelp;
	using AppAbout = Electux::App::View::About::AppAbout;
	using SettingsSetup = Electux::App::View::SettingsSetup;
	/// @}
	////////////////////////////////////////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// @class EntryApplication
	/// @brief Entry point for application and main application class.
	///
	/// Manages the application lifecycle, signal mapping, and coordinates
	/// between the configuration manager and the various UI views.
	////////////////////////////////////////////////////////////////////////////////////////////////////
	class EntryApplication : public Gtk::Application
	{
	public:
		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief EntryApplication constructor.
		////////////////////////////////////////////////////////////////////////////////////////////////
		explicit EntryApplication();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Creates a reference pointer to EntryApplication.
		/// @return A Glib::RefPtr to the newly created application instance.
		////////////////////////////////////////////////////////////////////////////////////////////////
		static Glib::RefPtr<EntryApplication> create();

	protected:
		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Handler for the startup signal to initialize application.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void on_startup() override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Handler for the activate signal to show main application.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void on_activate() override;

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Handler for the shutdown signal to perform cleanup.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void on_shutdown() override;

	private:
		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Maps signal/slots for application views and actions.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void mapping();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief On action settings create window for application settings.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void onActionSettings();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief On action doc create window for help documentation.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void onActionDoc();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief On action about create dialog for about application.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void onActionAbout();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief On action for quit application.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void onActionQuit();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief On handle close main window.
		/// @return true if the signal has been handled.
		////////////////////////////////////////////////////////////////////////////////////////////////
		bool onHandleClose();

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @brief Slot to handle setup signal change from settings window.
		/// @param setup Settings setup parameters for application.
		////////////////////////////////////////////////////////////////////////////////////////////////
		void onSetupChanged(const SettingsSetup &setup);

		////////////////////////////////////////////////////////////////////////////////////////////////
		/// @name Application Components
		/// @{
		/// @brief Instance of the configuration manager.
		std::unique_ptr<IConfig> m_configManager;
		/// @brief Main application window (home view).
		AppHome m_home{};
		/// @brief Settings window view.
		AppSettings m_settings{};
		/// @brief Help and documentation window view.
		AppHelp m_help{};
		/// @brief About dialog view.
		AppAbout m_about{};
		/// @}
		////////////////////////////////////////////////////////////////////////////////////////////////
	};
} // namespace Electux::App
