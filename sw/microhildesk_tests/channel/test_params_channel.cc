#include <gtest/gtest.h>
#include <params/channel_params.h>

using namespace Electux::App::Params::Channel;

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test bool to string.
///
/// @param ParamsChannelTest The name of the test case.
/// @param BoolToStringTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, BoolToStringTest) 
{
    EXPECT_EQ(boolToString(true), cTrue);
    EXPECT_EQ(boolToString(false), cFalse);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test empty string.
///
/// @param ParamsChannelTest The name of the test case.
/// @param EmptyStringTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, EmptyStringTest) 
{
    EXPECT_EQ(cEmptyString.length(), 0);
}


////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test number of channels.
///
/// @param ParamsChannelTest The name of the test case.
/// @param NumberOfChannelsTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, NumberOfChannelsTest) 
{
    EXPECT_EQ(cNumOfChannels, 8);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test space string.
///
/// @param ParamsChannelTest The name of the test case.
/// @param SpaceStringTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, SpaceStringTest)
{
    EXPECT_EQ(cSpaceString, " ");
    EXPECT_EQ(cSpaceString.length(), 1);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test space delimiter.
///
/// @param ParamsChannelTest The name of the test case.
/// @param SpaceDelimiterTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, SpaceDelimiterTest)
{
    EXPECT_EQ(cSpaceDelimiter, ' ');
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test true string.
///
/// @param ParamsChannelTest The name of the test case.
/// @param TrueStringTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, TrueStringTest)
{
    EXPECT_EQ(cTrue, "true");
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Test false string.
///
/// @param ParamsChannelTest The name of the test case.
/// @param FalseStringTest The name of the test.
////////////////////////////////////////////////////////////////////////////////////////////////////////
TEST(ParamsChannelTest, FalseStringTest)
{
    EXPECT_EQ(cFalse, "false");
}
